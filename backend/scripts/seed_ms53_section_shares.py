def main() -> None:
    db = SessionLocal()
    book = (
        db.query(ReferenceBook)
        .filter(ReferenceBook.code == BOOK_CODE, ReferenceBook.is_active == True)  # noqa: E712
        .first()
    )
    if not book:
        print(f"Книга {BOOK_CODE} не найдена")
        return

    deleted = (
        db.query(BookSectionShare)
        .filter(BookSectionShare.book_version_id == book.id)
        .delete()
    )
    if deleted:
        print(f"Удалено старых записей: {deleted}")

    n = 0
    for table_num, spec in SHARES.items():
        for stage in ("П", "Р"):
            total = sum(spec[stage].values())
            assert abs(total - 100) < 0.01, f"табл.{table_num} {stage}: сумма {total} != 100"
            stage_label = "ПД" if stage == "П" else "РД"
            for code, pct in spec[stage].items():
                db.add(BookSectionShare(
                    book_version_id=book.id,
                    table_num=table_num,
                    row_range=spec["src"],
                    stage=stage_label,
                    section_code=code,
                    section_name=NAMES[code],
                    pct=pct,
                ))
                n += 1
    db.commit()
    print(f"Посеяно {n} записей распределений для {BOOK_CODE} (book_id={book.id})")
    db.close()


if __name__ == "__main__":
    main()
