# Типы объектов создаются PER ПУНКТ таблицы (как в НЗ-53): у пунктов
# пересекаются диапазоны X, и без точного типа _match_row берёт первую
# попавшуюся строку. Имя типа = краткое описание пункта.
def _split_by_item(table_num: int, rows: list, title: str):
    # Ключ — полный row_num: интервалы X одного пункта делят row_num,
    # а подпункты (п.6.1 / п.6.2) — это разные объекты
    groups: dict[str, list] = {}
    for r in rows:
        groups.setdefault(r[0], []).append(r)
    return [
        (f"{grp[0][1][:80]} ({title} {item})", table_num, grp)
        for item, grp in groups.items()
    ]


