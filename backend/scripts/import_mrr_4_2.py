# «Барвиха Инвест» (индекс МКЭ II кв. 2026). ПРОВЕРИТЬ номер приказа МКЭ.
INDEX_2000 = (2026, 2, 9.923, "Кпер Москомэкспертизы (МКЭ) к уровню цен 01.01.2000, II кв. 2026 (из эталона Барвиха; номер приказа уточнить)")


def main() -> None:
    db = SessionLocal()

    old = db.query(ReferenceBook).filter(ReferenceBook.code == CODE).first()
    if old:
        for model in (ReferenceRow, BookObjectType, BookSectionShare, BookCondition):
            db.query(model).filter(model.book_version_id == old.id).delete()
        db.delete(old)
        db.flush()
        print("Старая версия удалена")

    book = ReferenceBook(
        code=CODE,
        official_name=(
            "Сборник 4.2 «Инженерные сети и сооружения. МРР-4.2.04-22» "
            "(в ред. от 30.09.2024). Московские региональные рекомендации. "
            "ПИЛОТ: таблицы 3.1, 3.3"
        ),
        version=1,
        status="consistent",
        is_active=True,
        price_base_year=2000,
        calc_method="standard",
        uploaded_at=datetime.utcnow(),
        notes=(
            "Региональная база для Москвы/МО. Кв=40/60 (табл.1 сборника) = дефолт МУ №620. "
            "Экстраполяция вверх по МРР ф.2.2 (+в×ΔX×0,5) движком пока не поддержана."
        ),
    )
    db.add(book)
    db.flush()

    n_rows = 0
    for name, table_num, rows in OBJECT_TYPES:
        ot = BookObjectType(book_version_id=book.id, name=name, table_num=table_num)
        db.add(ot)
        db.flush()
        for row_num, desc, unit, x_min, x_max, a, b in rows:
            db.add(ReferenceRow(
                book_version_id=book.id,
                object_type_id=ot.id,
                table_num=table_num,
                row_num=row_num,
                description=desc,
                x_unit=unit,
                x_min=x_min,
                x_max=x_max,
                a=a,
                b=b,
            ))
            n_rows += 1

    n_shares = 0
    for table_num, spec in SECTION_SHARES.items():
        for stage in ("ПД", "РД"):
            total = sum(spec[stage].values())
            assert abs(total - 100) < 0.01, f"МРР табл.{table_num} {stage}: {total} != 100"
            for code, pct in spec[stage].items():
                db.add(BookSectionShare(
                    book_version_id=book.id,
                    table_num=table_num,
                    row_range=spec["src"],
                    stage=stage,
                    section_code=code,
                    section_name=SECTION_NAMES[code],
                    pct=pct,
                ))
                n_shares += 1

    for table_num, key, cmin, cmax, short, full in CONDITIONS:
        db.add(BookCondition(
            book_version_id=book.id,
            table_num=table_num,
            condition_short=short,
            condition_text_full=full,
            effect_type="multiplier_range",
            coeff_min=cmin,
            coeff_max=cmax,
            coeff_key=key,
            apply_mode="multiply",
        ))

    year, quarter, value, src = INDEX_2000
    exists = db.query(PriceQuarterlyIndex).filter_by(
        year=year, quarter=quarter, base_year=2000, work_type="project"
    ).first()
    if not exists:
        db.add(PriceQuarterlyIndex(
            year=year, quarter=quarter, base_year=2000,
            work_type="project", index_value=value, source_ref=src,
        ))

    db.commit()
    print(f"Импортировано: книга {CODE} (id={book.id}), строк {n_rows}, "
          f"распределений {n_shares}, условий {len(CONDITIONS)}, индекс 2000→{value}")
    db.close()


if __name__ == "__main__":
    main()
