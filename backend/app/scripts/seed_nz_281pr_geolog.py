TABLE_TO_ROWS = {
    12: TABLE12_ROWS,
    14: TABLE14_ROWS,
    16: TABLE16_ROWS,
    18: TABLE18_ROWS,
}

TABLE_UNITS = {
    12: "га",
    14: "п.м",
    16: "п.м",
    18: "п.м",
}


def main():
    db = SessionLocal()
    try:
        # Upsert reference book
        book = (
            db.query(ReferenceBook)
            .filter(ReferenceBook.code == BOOK_CODE)
            .first()
        )
        if book:
            print(f"Book exists: {book.code} id={book.id}")
        else:
            book = ReferenceBook(
                code=BOOK_CODE,
                official_name=BOOK_NAME,
                version=1,
                status="consistent",
                is_active=True,
                price_base_year=2024,
                calc_method="standard",
                notes=(
                    "Базовые цены на 01.01.2024. Пересчёт в текущий уровень — "
                    "индекс из ФРСН (письмо Минстроя) для изысканий (work_type=survey). "
                    "Главы V–X (лабораторные, камеральные, технический отчёт, программа) "
                    "требуют дозагрузки."
                ),
            )
            db.add(book)
            db.flush()
            print(f"Created book: {book.code} id={book.id}")

        # Upsert object types
        otype_map = {}
        for otype_name, table_num in OBJECT_TYPES:
            otype = (
                db.query(BookObjectType)
                .filter(
                    BookObjectType.book_version_id == book.id,
                    BookObjectType.name == otype_name,
                )
                .first()
            )
            if not otype:
                otype = BookObjectType(
                    book_version_id=book.id,
                    name=otype_name,
                    table_num=table_num,
                )
                db.add(otype)
                db.flush()
                print(f"  + object_type: {otype_name} id={otype.id}")
            else:
                print(f"  = object_type exists: {otype_name} id={otype.id}")
            otype_map[table_num] = otype

        # Insert rows per table
        total_added = 0
        for table_num, rows in TABLE_TO_ROWS.items():
            x_unit = TABLE_UNITS[table_num]
            otype = otype_map[table_num]
            for (row_num, desc, x_min, x_max, a, b) in rows:
                existing = (
                    db.query(ReferenceRow)
                    .filter(
                        ReferenceRow.book_version_id == book.id,
                        ReferenceRow.table_num == table_num,
                        ReferenceRow.row_num == row_num,
                    )
                    .first()
                )
                if existing:
                    print(f"  = exists: табл.{table_num} {row_num}")
                    continue
                row = ReferenceRow(
                    book_version_id=book.id,
                    object_type_id=otype.id,
                    table_num=table_num,
                    row_num=row_num,
                    description=desc,
                    x_min=x_min,
                    x_max=x_max,
                    x_unit=x_unit,
                    a=a,
                    b=b,
                )
                db.add(row)
                total_added += 1
                print(f"  + табл.{table_num} {row_num}: a={a} b={b} [{x_unit}]")

        db.commit()
        print(f"\nDone. Added {total_added} rows.")
        print("MISSING: Tables 19-84+ (ударно-канатное продолжение, вибрационное,")
        print("  шнековое, ручное, закопушки, шурфы, опытно-фильтрационные,")
        print("  полевые испытания, лабораторные, камеральные, тех.отчёт, программа).")
    except Exception as exc:
        db.rollback()
        print(f"ERROR: {exc}", file=sys.stderr)
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()
