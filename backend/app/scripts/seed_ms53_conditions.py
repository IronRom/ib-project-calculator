def main() -> None:
    db = SessionLocal()
    try:
        book = db.query(ReferenceBook).filter(ReferenceBook.code == BOOK_CODE).first()
        if not book:
            raise SystemExit(f"Книга {BOOK_CODE} не найдена")

        deleted = (
            db.query(BookCondition)
            .filter(BookCondition.book_version_id == book.id)
            .delete()
        )

        for table_num, key, k_min, k_max, row_range, short in KEYED:
            db.add(BookCondition(
                book_version_id=book.id,
                table_num=table_num,
                row_range=row_range,
                condition_short=short,
                effect_type="multiplier_range",
                coeff_min=k_min,
                coeff_max=k_max,
                coeff_key=key,
                apply_mode="multiply",
            ))

        for table_num, k, short in REFERENCE_ONLY:
            db.add(BookCondition(
                book_version_id=book.id,
                table_num=table_num,
                row_range=None,
                condition_short=short,
                effect_type="multiplier_range",
                coeff_min=k,
                coeff_max=k,
                coeff_key=None,
                apply_mode=None,
            ))

        db.commit()
        total = db.query(BookCondition).filter(BookCondition.book_version_id == book.id).count()
        print(f"Deleted {deleted}, inserted {total} conditions for {BOOK_CODE} (book id={book.id})")
    finally:
        db.close()


if __name__ == "__main__":
    main()
