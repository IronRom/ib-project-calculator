def seed(book_code: str = "81-2001-17") -> None:
    db = SessionLocal()
    try:
        book = (
            db.query(ReferenceBook)
            .filter(ReferenceBook.is_active == True)
            .filter(ReferenceBook.code.ilike(f"%{book_code}%"))
            .first()
        )
        if not book:
            print(f"Active book '{book_code}' not found.")
            return

        deleted = db.query(BookExtractionHint).filter(
            BookExtractionHint.book_version_id == book.id
        ).delete()
        print(f"Deleted {deleted} existing hints")

        for h in HINTS:
            db.add(BookExtractionHint(book_version_id=book.id, **h))

        db.commit()
        print(f"Inserted {len(HINTS)} hints for {book.code}")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
