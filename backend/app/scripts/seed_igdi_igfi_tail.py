def _seed_rows(db, book_id: int, rows: list) -> int:
    tables = {r[0] for r in rows}
    db.query(ReferenceRow).filter(
        ReferenceRow.book_version_id == book_id,
        ReferenceRow.table_num.in_(list(tables)),
    ).update({"object_type_id": None}, synchronize_session=False)
    db.flush()
    db.query(BookObjectType).filter(
        BookObjectType.book_version_id == book_id,
        BookObjectType.table_num.in_(list(tables)),
    ).delete(synchronize_session=False)
    db.query(ReferenceRow).filter(
        ReferenceRow.book_version_id == book_id,
        ReferenceRow.table_num.in_(list(tables)),
    ).delete(synchronize_session=False)

    otypes: dict[tuple, BookObjectType] = {}
    n = 0
    for table_num, row_num, desc, x_min, x_max, x_unit, a, b, otype_name in rows:
        otype_id = None
        if otype_name:
            key = (table_num, otype_name)
            if key not in otypes:
                bot = BookObjectType(
                    book_version_id=book_id, name=otype_name,
                    table_num=table_num,
                    work_category="kameral" if table_num in (82, 83) else "field",
                )
                db.add(bot)
                db.flush()
                otypes[key] = bot
            otype_id = otypes[key].id
        db.add(ReferenceRow(
            book_version_id=book_id, table_num=table_num, row_num=row_num,
            description=desc, x_min=x_min, x_max=x_max, x_unit=x_unit,
            a=a, b=b, object_type_id=otype_id,
        ))
        n += 1
    return n


def main() -> None:
    db = SessionLocal()
    try:
        igdi = db.query(ReferenceBook).filter(ReferenceBook.code == IGDI).first()
        igfi = db.query(ReferenceBook).filter(ReferenceBook.code == IGFI).first()
        if not igdi or not igfi:
            raise SystemExit("Книги ИГДИ/ИГФИ не найдены")

        n1 = _seed_rows(db, igdi.id, IGDI_ROWS)
        n2 = _seed_rows(db, igfi.id, IGFI_ROWS)

        # ── conditions ────────────────────────────────────────────────────────
        for book, conds in ((igdi, IGDI_CONDITIONS), (igfi, IGFI_CONDITIONS)):
            keys = {c[1] for c in conds}
            db.query(BookCondition).filter(
                BookCondition.book_version_id == book.id,
                BookCondition.coeff_key.in_(list(keys)),
            ).delete(synchronize_session=False)
            for table_num, key, k, row_range, short in conds:
                db.add(BookCondition(
                    book_version_id=book.id, table_num=table_num,
                    row_range=row_range, condition_short=short,
                    effect_type="multiplier_range",
                    coeff_min=k, coeff_max=k, coeff_key=key,
                ))

        # ── ИГДИ К1 (=К2 табл.1) per field table ─────────────────────────────
        db.query(BookCondition).filter(
            BookCondition.book_version_id == igdi.id,
            BookCondition.coeff_key == "k1",
        ).delete(synchronize_session=False)
        override_map = {t: v for v, ts in IGDI_K1_OVERRIDES.items() for t in ts}
        field_tables = {
            t for (t,) in db.query(BookObjectType.table_num)
            .filter(BookObjectType.book_version_id == igdi.id,
                    BookObjectType.work_category == "field")
            .distinct().all() if t is not None
        } | set(override_map)
        for t in sorted(field_tables):
            k = override_map.get(t, IGDI_K1_DEFAULT)
            db.add(BookCondition(
                book_version_id=igdi.id, table_num=t,
                condition_short=(
                    f"ИГДИ табл.1 общих положений (К2): полевые по месту постоянной работы, К={k}"
                ),
                effect_type="multiplier_range",
                coeff_min=k, coeff_max=k, coeff_key="k1",
            ))

        # ── ИГФИ reference-only (К23/К24) ────────────────────────────────────
        for table_num, k, short in IGFI_REFERENCE_ONLY:
            db.add(BookCondition(
                book_version_id=igfi.id, table_num=table_num,
                condition_short=short, effect_type="multiplier_range",
                coeff_min=k, coeff_max=k, coeff_key=None,
            ))

        db.commit()
        c1 = db.query(BookCondition).filter(BookCondition.book_version_id == igdi.id).count()
        c2 = db.query(BookCondition).filter(BookCondition.book_version_id == igfi.id).count()
        print(f"ИГДИ: +{n1} строк, {c1} условий; ИГФИ: +{n2} строк, {c2} условий")
    finally:
        db.close()


if __name__ == "__main__":
    main()
