K1_DEFAULT = 0.66
K1_OVERRIDES = {70: 0.88, 72: 0.88, 74: 0.84}  # НЛС здания/помещения 0.88; сооружения 0.84 (табл.1 К2)


def main() -> None:
    db = SessionLocal()
    try:
        book = db.query(ReferenceBook).filter(ReferenceBook.code == IGDI).first()
        if not book:
            raise SystemExit("ИГДИ не найдена")

        tables = {r[0] for r in R}
        db.query(ReferenceRow).filter(
            ReferenceRow.book_version_id == book.id,
            ReferenceRow.table_num.in_(list(tables)),
        ).update({"object_type_id": None}, synchronize_session=False)
        db.flush()
        db.query(BookObjectType).filter(
            BookObjectType.book_version_id == book.id,
            BookObjectType.table_num.in_(list(tables)),
        ).delete(synchronize_session=False)
        db.query(ReferenceRow).filter(
            ReferenceRow.book_version_id == book.id,
            ReferenceRow.table_num.in_(list(tables)),
        ).delete(synchronize_session=False)

        otypes: dict[tuple, BookObjectType] = {}
        for table, rn, desc, xmin, xmax, unit, a, b, otype_name, cat in R:
            key = (table, otype_name)
            if key not in otypes:
                bot = BookObjectType(
                    book_version_id=book.id, name=otype_name,
                    table_num=table, work_category=cat,
                )
                db.add(bot)
                db.flush()
                otypes[key] = bot
            db.add(ReferenceRow(
                book_version_id=book.id, table_num=table, row_num=rn,
                description=desc, x_min=xmin, x_max=xmax, x_unit=unit,
                a=a, b=b, object_type_id=otypes[key].id,
            ))

        # k1 (К2 по месту постоянной работы) для новых полевых таблиц
        field_tables = {t for (t, _), bot in otypes.items() if bot.work_category == "field"}
        existing = {
            t for (t,) in db.query(BookCondition.table_num)
            .filter(BookCondition.book_version_id == book.id, BookCondition.coeff_key == "k1")
            .all()
        }
        for t in sorted(field_tables - existing):
            k = K1_OVERRIDES.get(t, K1_DEFAULT)
            db.add(BookCondition(
                book_version_id=book.id, table_num=t,
                condition_short=f"ИГДИ табл.1 общих положений (К2): полевые по месту постоянной работы, К={k}",
                effect_type="multiplier_range", coeff_min=k, coeff_max=k, coeff_key="k1",
            ))

        for table, k, short in REFERENCE_ONLY:
            exists = db.query(BookCondition).filter(
                BookCondition.book_version_id == book.id,
                BookCondition.table_num == table,
                BookCondition.coeff_key.is_(None),
                BookCondition.condition_short == short,
            ).first()
            if not exists:
                db.add(BookCondition(
                    book_version_id=book.id, table_num=table,
                    condition_short=short, effect_type="multiplier_range",
                    coeff_min=k, coeff_max=k, coeff_key=None,
                ))

        db.commit()
        n_rows = db.query(ReferenceRow).filter(ReferenceRow.book_version_id == book.id).count()
        n_conds = db.query(BookCondition).filter(BookCondition.book_version_id == book.id).count()
        print(f"ИГДИ: всего {n_rows} строк, {n_conds} условий (добавлено {len(R)} строк глав VII–IX)")
    finally:
        db.close()


if __name__ == "__main__":
    main()
