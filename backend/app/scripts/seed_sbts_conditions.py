def seed(book_code: str = "81-2001-17") -> None:
    db = SessionLocal()
    try:
        book = (
            db.query(ReferenceBook)
            .filter(ReferenceBook.is_active == True)
            .filter(ReferenceBook.code.ilike(f"%{book_code}%"))
            .first()
        )
        if not book:
            print(f"Active book '{book_code}' not found.")
            return

        deleted = db.query(BookCondition).filter(
            BookCondition.book_version_id == book.id
        ).delete()
        print(f"Deleted {deleted} existing conditions")

        for row in CONDITIONS:
            table_num, row_range, condition_short, effect_type, coeff_min, coeff_max, coeff_key = row
            db.add(BookCondition(
                book_version_id=book.id,
                table_num=table_num,
                row_range=row_range,
                condition_short=condition_short,
                effect_type=effect_type,
                coeff_min=coeff_min,
                coeff_max=coeff_max,
                coeff_key=coeff_key,
            ))

        db.commit()
        print(f"Inserted {len(CONDITIONS)} conditions for {book.code}")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
