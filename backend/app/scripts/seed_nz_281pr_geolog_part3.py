def main():
    db = SessionLocal()
    try:
        book = db.query(ReferenceBook).filter(ReferenceBook.code == BOOK_CODE).first()
        if not book:
            print(f"ERROR: book {BOOK_CODE} not found — run part1 script first", file=sys.stderr)
            sys.exit(1)
        print(f"Book: {book.code} id={book.id}")

        # ── Table 18 continuation: find existing object type ─────────────────
        otype_18 = (
            db.query(BookObjectType)
            .filter(
                BookObjectType.book_version_id == book.id,
                BookObjectType.table_num == 18,
            )
            .first()
        )
        if not otype_18:
            print("  WARNING: Table 18 object type not found, creating new", file=sys.stderr)
            otype_18 = BookObjectType(
                book_version_id=book.id,
                name="Бурение ударно-канатным способом (ИГИ)",
                table_num=18,
            )
            db.add(otype_18)
            db.flush()

        t18_added = 0
        for (row_num, desc, x_min, x_max, a, b) in TABLE18_CONT_ROWS:
            existing = (
                db.query(ReferenceRow)
                .filter(
                    ReferenceRow.book_version_id == book.id,
                    ReferenceRow.table_num == 18,
                    ReferenceRow.row_num == row_num,
                )
                .first()
            )
            if existing:
                continue
            db.add(ReferenceRow(
                book_version_id=book.id,
                object_type_id=otype_18.id,
                table_num=18,
                row_num=row_num,
                description=desc,
                x_min=x_min,
                x_max=x_max,
                x_unit="п.м",
                a=a,
                b=b,
            ))
            t18_added += 1
            print(f"  + табл.18 {row_num}: b={b}")
        print(f"  Table 18 continuation: +{t18_added} rows")

        # ── New object types ──────────────────────────────────────────────────
        otype_map = {}
        for otype_name, table_num, work_cat in NEW_OBJECT_TYPES:
            otype = (
                db.query(BookObjectType)
                .filter(
                    BookObjectType.book_version_id == book.id,
                    BookObjectType.name == otype_name,
                )
                .first()
            )
            if not otype:
                otype = BookObjectType(
                    book_version_id=book.id,
                    name=otype_name,
                    table_num=table_num,
                    work_category=work_cat,
                )
                db.add(otype)
                db.flush()
                print(f"  + object_type: {otype_name}")
            else:
                if otype.work_category != work_cat:
                    otype.work_category = work_cat
                print(f"  = object_type exists: {otype_name}")
            otype_map[table_num] = otype

        # ── Rows ──────────────────────────────────────────────────────────────
        total_added = 0
        for table_num, rows in TABLE_TO_ROWS.items():
            otype = otype_map[table_num]
            default_unit = TABLE_UNITS[table_num]
            for (row_num, desc, x_min, x_max, a, b) in rows:
                existing = (
                    db.query(ReferenceRow)
                    .filter(
                        ReferenceRow.book_version_id == book.id,
                        ReferenceRow.table_num == table_num,
                        ReferenceRow.row_num == row_num,
                    )
                    .first()
                )
                if existing:
                    continue
                # Table 33 has per-row units
                unit = TABLE33_UNITS.get(row_num, default_unit) if table_num == 33 else default_unit
                db.add(ReferenceRow(
                    book_version_id=book.id,
                    object_type_id=otype.id,
                    table_num=table_num,
                    row_num=row_num,
                    description=desc,
                    x_min=x_min,
                    x_max=x_max,
                    x_unit=unit,
                    a=a,
                    b=b,
                ))
                total_added += 1
                print(f"  + табл.{table_num} {row_num}: b={b}")

        db.commit()
        print(f"\nDone. Added {total_added} rows across new tables.")
    except Exception as exc:
        db.rollback()
        print(f"ERROR: {exc}", file=sys.stderr)
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()
